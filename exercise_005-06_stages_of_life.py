age = 65
if age < 2:
    print("You're a baby!")
elif age < 4:
    print("You're a toddler!")
elif age < 13:
    print("You're a kido!")
elif age < 20:
    print("You're a teenager!")
elif age < 65:
    print("You're an adult!")
elif age >= 65:
    print("You're an elder!")