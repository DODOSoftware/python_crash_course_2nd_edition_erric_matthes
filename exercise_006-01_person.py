# Exercise 006-1 Person. Create a dictionary and print every info:

person = {'first_name': 'michael',
          'middle_name': 'gerald',
          'last_name': 'tyson',
          'nickname': 'iron mike',
          'age': 56,
          'city': 'new york',
          }  # Comma after last pair

print(f"First name: {person['first_name'].title()}")
print(f"Middle name: {person['middle_name'].title()}")
print(f"Last name: {person['last_name'].title()}")
print(f"Nickname: {person['nickname'].title()}")
print(f"Age: {person['age']}")
print(f"City: {person['city'].title()}")
